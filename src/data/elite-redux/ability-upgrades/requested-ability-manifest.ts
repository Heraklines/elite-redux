/** The maintainer-requested operation applied to an existing ability. */
export type RequestedAbilityOperation = "add" | "replace" | "nerf" | "rename";

/** Broad subsystem used to organize implementation and regression coverage. */
export type RequestedAbilityGroup =
  | "status-control"
  | "damage-defense"
  | "healing-drain"
  | "items-meta"
  | "field-weather"
  | "follow-up"
  | "replacement"
  | "description";

/** A normalized row from the 2026-07-19 maintainer ability-overhaul request. */
export interface RequestedAbilityUpgrade {
  readonly name: string;
  /** Replacement display name for rename operations. */
  readonly newName?: string;
  /** ER draft id from er-abilities.ts, when the ability appears in that source list. */
  readonly erDraftId?: number;
  /** AbilityId enum key for a vanilla ability, when applicable. */
  readonly vanillaKey?: string;
  readonly operation: RequestedAbilityOperation;
  readonly group: RequestedAbilityGroup;
  readonly requestedEffect: string;
}

