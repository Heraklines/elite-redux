// =============================================================================
// Elite Redux — the BALANCE KNOB REGISTRY: the single source of truth for every
// game constant the team balancing editor may override via
// `er-balance-tuning.json`.
//
// Each knob carries its DEFAULT (mirroring the hardcoded value at its consumer
// site), its VALUE KIND and its CONSTRAINTS. The loader (er-balance-tuning.ts)
// validates every override against this registry and FALLS BACK TO THE DEFAULT
// for anything invalid — a bad committed value can never break the game, it is
// simply ignored (with a console warning).
//
// The editor SPA's "Game" tab is generated from this registry (the dump tool
// writes editor/data/balance-knobs.json), so label/help/grouping live here too.
//
// This file must stay DEPENDENCY-FREE (pure data) — it is imported by very
// early modules (er-run-difficulty and friends).
// =============================================================================

export type ErBalanceKnobKind = "scalar" | "array" | "pairs" | "map";

export interface ErBalanceKnob {
  /** Stable dotted key — the er-balance-tuning.json key. NEVER rename. */
  key: string;
  /** Editor group heading. */
  group: string;
  /** Short human label. */
  label: string;
  /** One-tooltip explanation for non-engineers (what does raising it do?). */
  help: string;
  kind: ErBalanceKnobKind;
  /** Default value (scalar: number; array: number[]; pairs: [number,number][]; map: Record<string,number>). */
  default: number | readonly number[] | ReadonlyArray<readonly [number, number]> | Readonly<Record<string, number>>;
  min: number;
  max: number;
  integer?: boolean;
  /** arrays/pairs: required element count (pairs: max count, min 1). */
  length?: number;
  /** arrays: required ordering of elements; pairs: ordering of FIRST elements (always asc) — "asc" allows equal neighbours. */
  ordering?: "asc" | "desc";
  /** Editor hint: show in the "advanced - edit with care" section. */
  advanced?: boolean;
}

